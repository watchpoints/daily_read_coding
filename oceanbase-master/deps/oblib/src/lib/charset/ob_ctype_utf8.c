static int ob_wildcmp_unicode_impl(const ObCharsetInfo* cs, const char* str, const char* str_end, const char* wildstr,
    const char* wildend, int escape, int w_one, int w_many, uint32_t** weights, int recurse_level)
{
  int result = -1; /* Not found, using wildcards */
  ob_wc_t s_wc = 0;
  ob_wc_t w_wc = 0;
  int scan_len = 0;
  ob_charset_conv_mb_wc mb_wc = cs->cset->mb_wc;

  /*if (ob_string_stack_guard && ob_string_stack_guard(recurse_level))
    return 1;*/
  while (wildstr != wildend) {
    while (1) {
      int escaped = 0;
      if ((scan_len = mb_wc((const unsigned char*)wildstr, (const unsigned char*)wildend, &w_wc)) <= 0) {
        return 1;
      }
      if (w_wc == (uint32_t)w_many) {
        result = 1; /* Found an anchor char */
        break;
      }
      wildstr += scan_len;

      if (w_wc == (uint32_t)escape && wildstr < wildend) {
        if ((scan_len = mb_wc((const unsigned char*)wildstr, (const unsigned char*)wildend, &w_wc)) <= 0) {
          return 1;
        }
        wildstr += scan_len;
        escaped = 1;
      }

      if ((scan_len = mb_wc((const unsigned char*)str, (const unsigned char*)str_end, &s_wc)) <= 0) {
        return 1;
      }
      str += scan_len;

      if (!escaped && w_wc == (uint32_t)w_one) {
        result = 1; /* Found an anchor char */
      } else {
        if (weights) {
          ob_tosort_unicode(weights, &s_wc);
          ob_tosort_unicode(weights, &w_wc);
        }
        if (s_wc != w_wc) {
          return 1; /* No match */
        }
      }
      if (wildstr == wildend) {
        return (str != str_end); /* Match if both are at end */
      }
    }

    if (w_wc == (uint32_t)w_many) { /* Found w_many */
      /* Remove any '%' and '_' from the wild search string */
      for (; wildstr != wildend;) {
        if ((scan_len = mb_wc((const unsigned char*)wildstr, (const unsigned char*)wildend, &w_wc)) <= 0) {
          return 1;
        }

        if (w_wc == (uint32_t)w_many) {
          wildstr += scan_len;
          continue;
        }

        if (w_wc == (uint32_t)w_one) {
          wildstr += scan_len;
          if ((scan_len = mb_wc((const unsigned char*)str, (const unsigned char*)str_end, &s_wc)) <= 0) {
            return 1;
          }
          str += scan_len;
          continue;
        }
        break; /* Not a wild character */
      }

      if (wildstr == wildend) {
        return 0; /* Ok if w_many is last */
      }

      if (str == str_end) {
        return -1;
      }

      if ((scan_len = mb_wc((const unsigned char*)wildstr, (const unsigned char*)wildend, &w_wc)) <= 0) {
        return 1;
      }
      wildstr += scan_len;

      if (w_wc == (uint32_t)escape) {
        if (wildstr < wildend) {
          if ((scan_len = mb_wc((const unsigned char*)wildstr, (const unsigned char*)wildend, &w_wc)) <= 0) {
            return 1;
          }
          wildstr += scan_len;
        }
      }

      /* Skip until the first character from wildstr is found */
      while (1) {
        while (str != str_end) {
          if ((scan_len = mb_wc((const unsigned char*)str, (const unsigned char*)str_end, &s_wc)) <= 0) {
            return 1;
          }
          if (weights) {
            ob_tosort_unicode(weights, &s_wc);
            ob_tosort_unicode(weights, &w_wc);
          }

          if (s_wc == w_wc) {
            break;
          }
          str += scan_len;
        }
        if (str == str_end) {
          return -1;
        }

        str += scan_len;
        result = ob_wildcmp_unicode_impl(
            cs, str, str_end, wildstr, wildend, escape, w_one, w_many, weights, recurse_level + 1);
        if (result <= 0)
          return result;
      }
    }
  }
  return (str != str_end ? 1 : 0);
}

int ob_wildcmp_unicode(const ObCharsetInfo* cs, const char* str, const char* str_end, const char* wildstr,
    const char* wildend, int escape, int w_one, int w_many, uint32_t** weights)
{
  return ob_wildcmp_unicode_impl(cs, str, str_end, wildstr, wildend, escape, w_one, w_many, weights, 1);
}

static int ob_wildcmp_utf8mb4(const ObCharsetInfo* cs, const char* str, const char* strend, const char* wildstr,
    const char* wildend, int escape, int w_one, int w_many)
{
  return ob_wildcmp_unicode(cs, str, strend, wildstr, wildend, escape, w_one, w_many, cs->caseinfo->sort_pages);
}

