size_t ob_strnxfrm_unicode_full_bin(const ObCharsetInfo* cs, unsigned char* dst, size_t dstlen, uint32_t nweights,
    const unsigned char* src, size_t srclen, int* is_valid_unicode)
{
  ob_wc_t wc;
  unsigned char* dst0 = dst;
  unsigned char* de = dst + dstlen;
  const unsigned char* se = src + srclen;
  wc = 0;
  ob_charset_assert(src);
  ob_charset_assert(cs->state & OB_CS_BINSORT);
  *is_valid_unicode = 1;
  for (; dst < de && nweights; nweights--) {
    int res;
    if ((res = cs->cset->mb_wc(src, se, &wc)) <= 0) {
      if (src < se) {
        *is_valid_unicode = 0;
      }
      break;
    }
    src += res;
    if ((res = cs->cset->wc_mb(wc, dst, de)) <= 0) {
      break;
    }
    dst += res;
  }

  return dst - dst0;
}

